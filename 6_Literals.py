# list literal
print()
fruits = ['apple', "mango", "orange"] 
print(fruits)
print()

# tuple literal
numbers = (1, 2, 3) 
print(numbers)
print()

# dictionary literal
alphabets = {'a':'apple', 'b':'ball', 'c':'cat'} 
print(alphabets)
print()

# set literal
vowels = {'a', 'e', 'i' , 'o', 'u'} 
print(vowels)
print()