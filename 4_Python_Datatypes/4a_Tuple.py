# In python it is similar to a list but we can't change the elements(which may be of different datatypes 
# like integer,float,list,string) of a tuple once it is assigned unlike list.

# To create a tuple we place all elements inside parentheses() separated by commas

# we can also create tuples without using parentheses

# Empty tuple
my_tuple = ()
print(my_tuple)

# tuple with mixed datatypes
my_tuple = (1, "Hello", 3.4)
print(my_tuple)

# nested tuple
my_tuple = ("mouse", [8, 4, 6], (1, 2, 3))
print(my_tuple)

my_tuple = 1, 2, 3,(1,2,4)
print(my_tuple)
