# Set is an unordered collection of unique items. 
# Set is defined by values separated by commas inside braces { }. 
# Since sets are unordered collections, indexing has no meaning.
#  Hence, the slicing operator [] does not work.


roll_no= {1,50,36,33,32}
student_id={112,321,721}

# Display roll no elements
print(roll_no)

# Display type of the roll no.
print(type(roll_no))

