# Python lists are mutable. 
# Meaning lists are changeable. 
# And, we can change items of a list by assigning new values using = operator. 

list=[1,2,'hello','world']

print("printing the initial list:",list)

# changing element of list at index 2 and 3
list[2]='hi'
list[3]='Shekhar kumar'

print('printing the list after changing the items of the list:',list)